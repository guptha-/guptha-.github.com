
public class MathCalcLong {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		//Tests for long operations
		long x= 20000;
		long y = 4000;
		long w = 0;
		//addition
		w = x + y;
		System.out.println("Sum of entered long = ");
		System.out.println(w);
		
		//subtraction
		long s = 0;
		s = x -y;
		System.out.println("Subtraction of entered long = ");
		System.out.println(s);
		
		//multiple
		long m = 0;
		m = x * y;
		System.out.println("Multiple long = ");
		System.out.println(m);
		
		//divide
		long d = 0;
		d = x / y;
		System.out.println("Divide long = ");
		System.out.println(d);

	}

}
