
public class MathCalcFloat {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		//Tests for Float operations
		float x= 2.4f;
		float y = 4.4f;
		float z = 0;
		//addition
		z = x +y;
		System.out.println("Sum of entered float = ");
		System.out.println(z);
		
		//subtraction
		float s = 0;
		s = y -x;
		System.out.println("Subtraction of entered float = ");
		System.out.println(s);
		
		//multiple
		float m = 0;
		m = x * y;
		System.out.println("Multiple float = ");
		System.out.println(m);
		
		//divide
		float d = 0;
		d = y / x;
		System.out.println("Divide float = ");
		System.out.println(d);

	}

}
